class Burger {
  final String id;
  final String name;
  final String subtitle;
  final double rating;
  final int timeMins;
  final String description;
  final double price;
  final String imageUrl;

  Burger({
    required this.id,
    required this.name,
    required this.subtitle,
    required this.rating,
    required this.timeMins,
    required this.description,
    required this.price,
    required this.imageUrl,
  });
}
